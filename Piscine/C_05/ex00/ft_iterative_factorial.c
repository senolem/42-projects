int	ft_iterative_factorial(int nb);

int	ft_iterative_factorial(int nb)
{
	int		i;
	long	fact;

	i = 1;
	fact = 1;
	if (nb < 0)
		return (0);
	while (i <= nb)
	{
		fact = fact * i;
		i++;
	}
	return (fact);
}
