int	ft_strcmp(char *s1, char *s2)
{
	int	r;

	r = 0;
	while ((s1[r] == s2[r]) && (s1[r] != '\0') && (s2[r] != '\0'))
		r++;
	return (s1[r] - s2[r]);
}
